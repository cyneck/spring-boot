package cn.evun.sweet.framework.common.network.http;

public class HttpVisitException extends Exception{
	
	   public HttpVisitException() {
	        super();
	    }

	    public HttpVisitException(String message) {
	        super(message);
	    }
	    
	    public HttpVisitException(String message, Throwable cause) {
	        super(message, cause);
	    }
	    
	    public HttpVisitException(Throwable cause) {
	        super(cause);
	    }

	
}
